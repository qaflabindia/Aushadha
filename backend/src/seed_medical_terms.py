def seed_medical_terms():
    """Insert all medical terms into the translation cache."""
    ensure_table()
    db = SessionLocal()
    count = 0
    try:
        for en_term, translations in MEDICAL_TERMS.items():
            for lang_code, translated in translations.items():
                save_to_cache(
                    db=db,
                    source_text=en_term,
                    source_lang="en",
                    target_lang=lang_code,
                    translated_text=translated,
                    is_medical_term=True,
                )
                count += 1
        logger.info(f"Seeded {count} medical term translations.")
    finally:
        db.close()


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    seed_medical_terms()
